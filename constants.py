def ncs_unit_names_for_prompt() -> str:
    """AI 프롬프트용: 전자 능력단위명을 앞에 두고 나머지를 이어 붙인다."""
    rest = [k for k in NCS_DB if k not in ELECTRONICS_NCS_UNITS]
    return ", ".join(list(ELECTRONICS_NCS_UNITS) + rest)


def format_ncs_unit(unit_name: str) -> str:
    """능력단위를 '명칭 (코드)' 형식으로 반환. 코드 없으면 명칭만."""
    if not unit_name:
        return ""
    meta = NCS_DB.get(unit_name, {})
    code = meta.get("code", "")
    return f"{unit_name} ({code})" if code else unit_name


def format_ncs_unit_short(unit_name: str, max_len: int = 20) -> str:
    """차트·테이블용 짧은 표시. 길면 코드를 다음 줄로."""
    if not unit_name:
        return ""
    meta = NCS_DB.get(unit_name, {})
    code = meta.get("code", "")
    if not code:
        return unit_name
    if len(unit_name) + len(code) + 4 <= max_len:
        return f"{unit_name} ({code})"
    return f"{unit_name}<br><small>({code})</small>"


